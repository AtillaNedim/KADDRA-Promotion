function generate() {
    var html = "";
    for (var i = 0; i < text["lyrics"].length; i++) {
        html += "<div";
        if (i == 0) {
            html += ` class="highlighted"`;
            currentLine = 0;
        }
        if (text["lyrics"][i]["note"]) {
            html += ` note="${text["lyrics"][i]["note"]}"`;
        }
        html += ">";
        html +=
            text["lyrics"][i]["line"] == "" ? "•" : text["lyrics"][i]["line"];
        html += "</div>";
    }
    $(".lyrics").html(html);
    align();
}